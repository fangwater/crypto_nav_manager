CREATE TABLE strategy_envs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug TEXT NOT NULL UNIQUE,
    alias TEXT NOT NULL UNIQUE,
    db_schema TEXT NOT NULL UNIQUE
        CHECK (db_schema ~ '^[a-z][a-z0-9_]*$'),
    host TEXT NOT NULL,
    env_path TEXT NOT NULL,
    csv_output_dir TEXT NOT NULL,
    exchange TEXT NOT NULL,
    account_mode TEXT NOT NULL,
    strategy_kind TEXT NOT NULL,
    required_keys JSONB NOT NULL
        CHECK (jsonb_typeof(required_keys) = 'array'),
    config_url TEXT NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (host, env_path)
);

CREATE INDEX idx_strategy_envs_enabled_order
    ON strategy_envs(enabled, sort_order);

