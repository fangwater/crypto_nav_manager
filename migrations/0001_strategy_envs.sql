CREATE OR REPLACE FUNCTION ensure_strategy_storage(target_schema TEXT)
RETURNS VOID
LANGUAGE plpgsql
AS $$
BEGIN
    IF target_schema !~ '^[a-z][a-z0-9_]*$' THEN
        RAISE EXCEPTION 'invalid strategy schema: %', target_schema;
    END IF;

    EXECUTE format('CREATE SCHEMA IF NOT EXISTS %I', target_schema);

    EXECUTE format(
        'CREATE TABLE IF NOT EXISTS %I.trade_fills (
            market TEXT NOT NULL,
            symbol TEXT NOT NULL,
            trade_id TEXT NOT NULL,
            order_id TEXT,
            side TEXT,
            liquidity_role TEXT,
            price NUMERIC(38, 18) NOT NULL,
            quantity NUMERIC(38, 18) NOT NULL,
            quote_quantity NUMERIC(38, 18),
            fee_amount NUMERIC(38, 18),
            fee_asset TEXT,
            fee_usdt NUMERIC(38, 18),
            realized_pnl NUMERIC(38, 18),
            event_time_ms BIGINT NOT NULL CHECK (event_time_ms >= 0),
            raw JSONB NOT NULL,
            fetched_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
            PRIMARY KEY (market, symbol, trade_id)
        )',
        target_schema
    );

    EXECUTE format(
        'CREATE INDEX IF NOT EXISTS %I ON %I.trade_fills (event_time_ms)',
        target_schema || '_trade_fills_time_idx',
        target_schema
    );

    EXECUTE format(
        'CREATE INDEX IF NOT EXISTS %I ON %I.trade_fills (symbol, event_time_ms)',
        target_schema || '_trade_fills_symbol_time_idx',
        target_schema
    );

    EXECUTE format(
        'CREATE TABLE IF NOT EXISTS %I.funding_fees (
            record_id TEXT PRIMARY KEY,
            symbol TEXT,
            asset TEXT,
            amount NUMERIC(38, 18) NOT NULL,
            amount_usdt NUMERIC(38, 18),
            funding_rate NUMERIC(38, 18),
            event_time_ms BIGINT NOT NULL CHECK (event_time_ms >= 0),
            raw JSONB NOT NULL,
            fetched_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
        )',
        target_schema
    );

    EXECUTE format(
        'CREATE INDEX IF NOT EXISTS %I ON %I.funding_fees (event_time_ms)',
        target_schema || '_funding_fees_time_idx',
        target_schema
    );

    EXECUTE format(
        'CREATE TABLE IF NOT EXISTS %I.borrow_interest (
            record_id TEXT PRIMARY KEY,
            symbol TEXT,
            asset TEXT NOT NULL,
            amount NUMERIC(38, 18) NOT NULL,
            amount_usdt NUMERIC(38, 18),
            interest_rate NUMERIC(38, 18),
            event_time_ms BIGINT NOT NULL CHECK (event_time_ms >= 0),
            raw JSONB NOT NULL,
            fetched_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
        )',
        target_schema
    );

    EXECUTE format(
        'CREATE INDEX IF NOT EXISTS %I ON %I.borrow_interest (event_time_ms)',
        target_schema || '_borrow_interest_time_idx',
        target_schema
    );
END;
$$;

SELECT ensure_strategy_storage(db_schema)
FROM strategy_envs
ORDER BY sort_order;
